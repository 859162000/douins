package com.douins.statics.domain.vo;

import com.douins.statics.domain.model.Image;

/** 
* @ClassName: ImageVo 
* @Description: TODO(这里用一句话描述这个类的作用) 
* @author G. F. 
* @date 2015年11月19日 下午6:05:55 
*  
*/
public class ImageVo {
        private Image image;
        
        public ImageVo(Image img){
            this.image = img;
        }

        /**
         * @return
         * @see com.douins.statics.domain.model.Image#getName()
         */
        public String getName() {
            return image.getName();
        }

        /**
         * @return
         * @see com.douins.statics.domain.model.Image#getUrl()
         */
        public String getUrl() {
            return image.getUrl();
        }
        
        
        public String getTargetUrl() {
            return image.getTargetUrl();
        }
        
        
}
