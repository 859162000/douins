package com.douins.bank.domain.vo;

public class BankChangeResponseVo {
	
	private String accountStatus;
	private String passwordStatus;
	private String userStatus;
	private String setPayPasswordUrl; //设置支付密码
	private String modifyPayPasswordUrl; //修改支付密码
	private String resetPayPasswordUrl;//重置支付密码
	private String rechargeUrl; //充值
	private String withdrawUrl; //提现
	
	public String getSetPayPasswordUrl() {
		return setPayPasswordUrl;
	}
	public void setSetPayPasswordUrl(String setPayPasswordUrl) {
		this.setPayPasswordUrl = setPayPasswordUrl;
	}
	public String getModifyPayPasswordUrl() {
		return modifyPayPasswordUrl;
	}
	public void setModifyPayPasswordUrl(String modifyPayPasswordUrl) {
		this.modifyPayPasswordUrl = modifyPayPasswordUrl;
	}
	public String getResetPayPasswordUrl() {
		return resetPayPasswordUrl;
	}
	public void setResetPayPasswordUrl(String resetPayPasswordUrl) {
		this.resetPayPasswordUrl = resetPayPasswordUrl;
	}
	public String getRechargeUrl() {
		return rechargeUrl;
	}
	public void setRechargeUrl(String rechargeUrl) {
		this.rechargeUrl = rechargeUrl;
	}
	public String getWithdrawUrl() {
		return withdrawUrl;
	}
	public void setWithdrawUrl(String withdrawUrl) {
		this.withdrawUrl = withdrawUrl;
	}
	
	public String getAccountStatus() {
		return accountStatus;
	}
	public void setAccountStatus(String accountStatus) {
		this.accountStatus = accountStatus;
	}
	public String getPasswordStatus() {
		return passwordStatus;
	}
	public void setPasswordStatus(String passwordStatus) {
		this.passwordStatus = passwordStatus;
	}
	public String getUserStatus() {
		return userStatus;
	}
	public void setUserStatus(String userStatus) {
		this.userStatus = userStatus;
	}
}
