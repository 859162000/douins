package com.douins.agency.service.insurance.lianlife.domain.model;
import com.thoughtworks.xstream.annotations.XStreamAlias;

/**
 * @Description: 受益人信息
 * @author pan
 * @date 2016年03月04日
 */
@XStreamAlias("rebeneficiary")
public class Benifit {
	/*
	 * 受益人姓名
	 */
	@XStreamAlias("beneficiaryname")
	private String beneficiaryName;
	/*
	 * 受益人证件号码
	 */
	@XStreamAlias("identifynumber")
	private String identifyNumber;
	/*
	 * 受益人证件类型
	 */
	@XStreamAlias("identifytype")
	private String identifyType;
	/*
	 * 受益人年龄
	 */
	private String age;
	/*
	 * 受益人性别
	 */
	private String sex;
	/*
	 * 受益人出生日期
	 */
	@XStreamAlias("birthday")
	private String birthDay;
	/*
	 * 受益人地址
	 */
	@XStreamAlias("beneficiaryaddress")
	private String beneficiaryAddress;
	/*
	 * 受益人邮政编码
	 */
	@XStreamAlias("postcode")
	private String postCode;
	/*
	 * 受益人联系电话
	 */
	@XStreamAlias("phonenumber")
	private String phoneNumber;
	/*
	 * 受益人移动电话
	 */
	private String mobile;
	/*
	 * 受益人邮箱
	 */
	private String email;
	/*
	 * 受益人与被保险人关系
	 */
	private String relation;
	/*
	 * 受益份额
	 */
	private String share;
	public String getBeneficiaryName() {
		return beneficiaryName;
	}
	public void setBeneficiaryName(String beneficiaryName) {
		this.beneficiaryName = beneficiaryName;
	}
	public String getIdentifyNumber() {
		return identifyNumber;
	}
	public void setIdentifyNumber(String identifyNumber) {
		this.identifyNumber = identifyNumber;
	}
	public String getIdentifyType() {
		return identifyType;
	}
	public void setIdentifyType(String identifyType) {
		this.identifyType = identifyType;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getBirthDay() {
		return birthDay;
	}
	public void setBirthDay(String birthDay) {
		this.birthDay = birthDay;
	}
	public String getBeneficiaryAddress() {
		return beneficiaryAddress;
	}
	public void setBeneficiaryAddress(String beneficiaryAddress) {
		this.beneficiaryAddress = beneficiaryAddress;
	}
	public String getPostCode() {
		return postCode;
	}
	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getRelation() {
		return relation;
	}
	public void setRelation(String relation) {
		this.relation = relation;
	}
	public String getShare() {
		return share;
	}
	public void setShare(String share) {
		this.share = share;
	}
	
	
}
