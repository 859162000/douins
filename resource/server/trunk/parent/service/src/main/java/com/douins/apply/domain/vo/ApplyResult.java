package com.douins.apply.domain.vo;

import java.io.Serializable;
import java.util.Date;

import com.douins.apply.domain.model.ApplyInfo;


public class ApplyResult implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 6145573923177260375L;
	private boolean isSuccess;
	private String resultCode;
	private String msg;
	private ApplyInfo applyInfo;
	private String virtualAccountNo;
	private Date finishTime;
	
	public String getVirtualAccountNo() {
		return virtualAccountNo;
	}
	public void setVirtualAccountNo(String virtualAccountNo) {
		this.virtualAccountNo = virtualAccountNo;
	}
	public ApplyInfo getApplyInfo() {
		return applyInfo;
	}
	public void setApplyInfo(ApplyInfo applyInfo) {
		this.applyInfo = applyInfo;
	}
	public boolean isSuccess() {
		return isSuccess;
	}
	public void setSuccess(boolean isSuccess) {
		this.isSuccess = isSuccess;
	}
	public String getResultCode() {
		return resultCode;
	}
	public void setResultCode(String resultCode) {
		this.resultCode = resultCode;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public Date getFinishTime() {
		return finishTime;
	}
	public void setFinishTime(Date finishTime) {
		this.finishTime = finishTime;
	}
	
}
