package com.douins.insurance.domain.modelLianlife;

import com.thoughtworks.xstream.annotations.XStreamAlias;

public class ApplyInfoItem {
	@XStreamAlias("ApplyNoList")
	private ApplyNoList applyNoList;
	@XStreamAlias("IsSuccess")
	private String issuccess;
	@XStreamAlias("OrderID")
	private String orderid;
	@XStreamAlias("TotalPremium")
	private String totalpremium;
	@XStreamAlias("AccountDate")
	private String accountdate;
	@XStreamAlias("ApplicantName")
	private String applicantname;
	@XStreamAlias("FailReason")
	private String failReason;
	
	public ApplyNoList getApplyNoList() {
		return applyNoList;
	}
	public void setApplyNoList(ApplyNoList applyNoList) {
		this.applyNoList = applyNoList;
	}
	public String getIssuccess() {
		return issuccess;
	}
	public void setIssuccess(String issuccess) {
		this.issuccess = issuccess;
	}
	public String getOrderid() {
		return orderid;
	}
	public void setOrderid(String orderid) {
		this.orderid = orderid;
	}
	public String getTotalpremium() {
		return totalpremium;
	}
	public void setTotalpremium(String totalpremium) {
		this.totalpremium = totalpremium;
	}
	public String getAccountdate() {
		return accountdate;
	}
	public void setAccountdate(String accountdate) {
		this.accountdate = accountdate;
	}
	public String getApplicantname() {
		return applicantname;
	}
	public void setApplicantname(String applicantname) {
		this.applicantname = applicantname;
	}
	public String getFailReason() {
		return failReason;
	}
	public void setFailReason(String failReason) {
		this.failReason = failReason;
	}
	

}
