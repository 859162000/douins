package com.douins.agency.service.channel.qunar.domain.model;

import com.thoughtworks.xstream.annotations.XStreamAlias;

/** 
* @ClassName: PolicyConfirm 
* @Description: 承保请求中的 policyConfirm
* @author G. F. 
* @date 2015年12月30日 下午11:07:02 
*  
*/
@XStreamAlias("parameter")
public class PolicyConfirm {
    @XStreamAlias("bankseqno")
    private String bankSeqNo;
    @XStreamAlias("banktradedate")
    private String bankTradeDate;
    @XStreamAlias("paytype")
    private String payType;
    @XStreamAlias("orderno")
    private String orderNo;
    @XStreamAlias("payment")
    private String payment;
    @XStreamAlias("businessid")
    private String businessId;
    @XStreamAlias("agencyno")
    private String agencyNo;
    
    private String tradeNo;
    public String getTradeNo() {
		return tradeNo;
	}
	public void setTradeNo(String tradeNo) {
		this.tradeNo = tradeNo;
	}
	public String getBankSeqNo() {
        return bankSeqNo;
    }
    public void setBankSeqNo(String bankSeqNo) {
        this.bankSeqNo = bankSeqNo;
    }
    public String getBankTradeDate() {
        return bankTradeDate;
    }
    public void setBankTradeDate(String bankTradeDate) {
        this.bankTradeDate = bankTradeDate;
    }
    public String getPayType() {
		return payType;
	}
	public void setPayType(String payType) {
		this.payType = payType;
	}
	public String getOrderNo() {
        return orderNo;
    }
    public void setOrderNo(String orderNo) {
        this.orderNo = orderNo;
    }
    public String getPayment() {
        return payment;
    }
    public void setPayment(String payment) {
        this.payment = payment;
    }
    public String getBusinessId() {
        return businessId;
    }
    public void setBusinessId(String businessId) {
        this.businessId = businessId;
    }
    public String getAgencyNo() {
        return agencyNo;
    }
    public void setAgencyNo(String agencyNo) {
        this.agencyNo = agencyNo;
    }
}
