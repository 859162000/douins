package com.douins.insurance.domain.model;

import java.math.BigDecimal;
import java.util.Date;

import com.thoughtworks.xstream.annotations.XStreamAlias;

@XStreamAlias("paymentMethod")
public class PaymentMethod {
	private String payMode;
	private String payDate;
	private String payTime;
	private BigDecimal payMoney;
	private String payCheckNo;
	private String bankCode;
	private String bankAccount;
	private String accoutName;
	private String bankAccountCity;
	private String orderId;
	private String proposalNo;
	private BigDecimal totalPrem;
	private String sendTime;
	
	public String getPayMode() {
		return payMode;
	}
	public void setPayMode(String payMode) {
		this.payMode = payMode;
	}
	public String getPayDate() {
		return payDate;
	}
	public void setPayDate(String payDate) {
		this.payDate = payDate;
	}
	public String getPayTime() {
		return payTime;
	}
	public void setPayTime(String payTime) {
		this.payTime = payTime;
	}
	public BigDecimal getPayMoney() {
		return payMoney;
	}
	public void setPayMoney(BigDecimal payMoney) {
		this.payMoney = payMoney;
	}
	public String getPayCheckNo() {
		return payCheckNo;
	}
	public void setPayCheckNo(String payCheckNo) {
		this.payCheckNo = payCheckNo;
	}
	public String getBankCode() {
		return bankCode;
	}
	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}
	public String getBankAccount() {
		return bankAccount;
	}
	public void setBankAccount(String bankAccount) {
		this.bankAccount = bankAccount;
	}
	public String getAccoutName() {
		return accoutName;
	}
	public void setAccoutName(String accoutName) {
		this.accoutName = accoutName;
	}
	public String getBankAccountCity() {
		return bankAccountCity;
	}
	public void setBankAccountCity(String bankAccountCity) {
		this.bankAccountCity = bankAccountCity;
	}
    public String getOrderId() {
        return orderId;
    }
    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }
    public String getProposalNo() {
        return proposalNo;
    }
    public void setProposalNo(String proposalNo) {
        this.proposalNo = proposalNo;
    }
    public BigDecimal getTotalPrem() {
        return totalPrem;
    }
    public void setTotalPrem(BigDecimal totalPrem) {
        this.totalPrem = totalPrem;
    }
    public String getSendTime() {
        return sendTime;
    }
    public void setSendTime(String sendTime) {
        this.sendTime = sendTime;
    }
  
}
