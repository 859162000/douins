package com.douins.apply.domain.vo;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;

public class SurrenderInfoVo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8194524770963851463L;
	private String policyId;
	private String policyCode;
	private BigDecimal totalPrem;//
	private Date payTime;
	private BigDecimal repayAmount;
	private BigDecimal handFee;
	
	private BigDecimal revenue;        // 收益
	
	
	public String getPolicyId() {
		return policyId;
	}

	public void setPolicyId(String policyId) {
		this.policyId = policyId;
	}

	public String getPolicyCode() {
		return policyCode;
	}

	public void setPolicyCode(String policyCode) {
		this.policyCode = policyCode;
	}

	public BigDecimal getTotalPrem() {
		return totalPrem;
	}

	public void setTotalPrem(BigDecimal totalPrem) {
		this.totalPrem = totalPrem;
	}

	public Date getPayTime() {
		return payTime;
	}

	public void setPayTime(Date payTime) {
		this.payTime = payTime;
	}

	public BigDecimal getRepayAmount() {
		return repayAmount;
	}

	public void setRepayAmount(BigDecimal repayAmount) {
		this.repayAmount = repayAmount;
	}

	public BigDecimal getHandFee() {
		return handFee;
	}

	public void setHandFee(BigDecimal handFee) {
		this.handFee = handFee;
	}

    public BigDecimal getRevenue() {
        return revenue;
    }

    public void setRevenue(BigDecimal revenue) {
        this.revenue = revenue;
    }
	
}
