package com.douins.insurance.domain.modelLianlife;

import com.thoughtworks.xstream.annotations.XStreamAlias;

public class RefundInfoItem {
	@XStreamAlias("PolicyNo")
	private String policyno;
	private String applicantname;
	@XStreamAlias("RefundTime")
	private String refundtime;
	@XStreamAlias("FailReason")
	private String failReason;
	@XStreamAlias("IDNO")
	private String idNo;
	@XStreamAlias("IsSuccess")
	private String isSuccess;
	@XStreamAlias("ApplicantName")
	private String applicantName;
	@XStreamAlias("RefundAmount")
	private String refundAmount;
	public String getPolicyno() {
		return policyno;
	}
	public void setPolicyno(String policyno) {
		this.policyno = policyno;
	}
	public String getApplicantname() {
		return applicantname;
	}
	public void setApplicantname(String applicantname) {
		this.applicantname = applicantname;
	}
	public String getRefundtime() {
		return refundtime;
	}
	public void setRefundtime(String refundtime) {
		this.refundtime = refundtime;
	}
	public String getFailReason() {
		return failReason;
	}
	public void setFailReason(String failReason) {
		this.failReason = failReason;
	}
	public String getIdNo() {
		return idNo;
	}
	public void setIdNo(String idNo) {
		this.idNo = idNo;
	}
	public String getIsSuceess() {
		return isSuccess;
	}
	public void setIsSuceess(String isSuceess) {
		this.isSuccess = isSuceess;
	}
	

}
