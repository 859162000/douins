package com.douins.agency.service.insurance.ccic.domain.model;

import com.thoughtworks.xstream.annotations.XStreamAlias;

/**
 * @Description: 批单信息
 * @author hou
 * @date 2015年12月28日
 */
@XStreamAlias("endorse")
public class Endorse {
	/*
	 * 批单号
	 */
    @XStreamAlias("endorseno")
	private String endorseNo;
	/*
	 * 批单印刷号
	 */
    @XStreamAlias("printno")
	private String printNo;
	/*
	 * 退还保费
	 */
    @XStreamAlias("chgpremium")
	private String chgPremium;
	/*
	 * 批文
	 */
    @XStreamAlias("endorsetext")
	private String endorseText;
	
	public String getEndorseNo() {
		return endorseNo;
	}
	public void setEndorseNo(String endorseNo) {
		this.endorseNo = endorseNo;
	}
	public String getPrintNo() {
		return printNo;
	}
	public void setPrintNo(String printNo) {
		this.printNo = printNo;
	}
	public String getChgPremium() {
		return chgPremium;
	}
	public void setChgPremium(String chgPremium) {
		this.chgPremium = chgPremium;
	}
	public String getEndorseText() {
		return endorseText;
	}
	public void setEndorseText(String endorseText) {
		this.endorseText = endorseText;
	}
	
}
